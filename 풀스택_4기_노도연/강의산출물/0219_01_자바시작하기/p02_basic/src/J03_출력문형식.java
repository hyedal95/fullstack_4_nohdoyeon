public class J03_출력문형식 {
    public static void main(String[] args) {
        //변수 타입 설정 = 값 설정
        String name= "노도연";  // 문자열 자료형(String 타입 Type ) - 이름(name)
        int age= 30;            // 숫자 정수 자료형(int 타입 Type ) - 나이(age)
        
        System.out.println();
        System.out.println("이름은" + name + "입니다.");
        System.out.println("나이는" + age + "입니다.");
        System.out.println();

        
        /*  ----결과 Result----
            이름은노도연입니다.
            나이는30입니다. 
        */

    }
}
